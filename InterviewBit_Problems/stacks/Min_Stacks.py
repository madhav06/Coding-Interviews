class Solution:

    def isEmpty(self):
        my_stack.x == []

    def push(self, x):
        return my_stack.x.append()

    def pop(self):
        return my_stack.pop()

    def top(self):
        return my_stack.top()

    def getMin(self, x):
        if top() == 0:
            return -1
        if top != 0:
            if min(x.append):
                return x.append
            return top()

    def __init__(self):
        my_stack = []

